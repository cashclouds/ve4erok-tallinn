// Функция для получения размышления по дате
function getJFTByDate(month, day) {
    const dateKey = `${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;

    // Проверяем, есть ли запись в расширенной базе
    if (jftDataExtended[dateKey]) {
        return { date: dateKey, ...jftDataExtended[dateKey] };
    }

    // Если нет, генерируем на основе шаблонов
    return generateDefaultJFT(month, day);
}

